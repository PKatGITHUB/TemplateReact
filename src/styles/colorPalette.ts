export const getHealthColorGivenValue = (
	healthScore: number,
	theme: TGlobalThemeObject
) => {
	if (healthScore > 7) {
		return theme.green;
	} else if (healthScore > 4) {
		return theme.yellow;
	} else {
		return theme.red;
	}
};

